package com.blockFileExtension.vo;

public class ExtensionInfo {
    private Integer funcKey;
    private Integer exKey;
    private String code;
    private Boolean defaultYn;

    public Integer getFuncKey() {
        return funcKey;
    }

    public void setFuncKey(Integer funcKey) {
        this.funcKey = funcKey;
    }

    public Integer getExKey() {
        return exKey;
    }

    public void setExKey(Integer exKey) {
        this.exKey = exKey;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public Boolean getDefaultYn() {
        return defaultYn;
    }

    public void setDefaultYn(Boolean defaultYn) {
        this.defaultYn = defaultYn;
    }
}
